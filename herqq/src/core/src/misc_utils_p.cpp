#include "misc_utils_p.h"

namespace Herqq
{

unsigned long hash(const char *str, int n)
{
    int c;
    unsigned long hash_value = 5381;

    while (n--)
    {
        c = *str++;
        hash_value = ((hash_value << 5) + hash_value) + c; // hash_value*33 + c
    }

    return hash_value;
}

}
